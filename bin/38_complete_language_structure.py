# 1. What python library means?
# ---------
# - python library can be 1 or more modules like mymodule.py
# - python library can be 1 or more packages like mypackage
# - python library can be 1 or more frameworks
#   -- frameworks are like mymodule.py/mypackage
#   -- Difference:
#       - in mymodule/mypackage we are keeping only definintions
#       - BUT in framework some common tasks are implemented
#           example: unittest framework
#           writing test cases for different project is different
#           but executing all tests, producing report etc are common
#           so these common things are implemented
